export type StringKey = keyof typeof strings['en'];
export type Strings = { [K in StringKey]: typeof strings['en'][K] };

export function t(lang: Lang): Strings {
  return strings[lang] as Strings;
}

/** Return the localized summit name, falling back gracefully */
export function summitName(
  summit: { name_ko: string; name_en?: string | null; name_ja?: string | null },
  lang: Lang,
): string {
  if (lang === 'en' && summit.name_en) return summit.name_en;
  if (lang === 'ja' && summit.name_ja) return summit.name_ja;
  return summit.name_ko;
}
